!======================================================================
      Integer Function Itrans(l,q,nj1,nj2,kt1,kt2,JT,kt,C)
!======================================================================
      Use lq_jj_ls

      Implicit none
      Integer, intent(in) :: l,q,nj1,nj2,kt1,kt2,JT,kt
      Real(8) :: C
      Integer :: m
      Integer, external :: Jtrans
      m = 4*l+2; Itrans=1; C=1.d0
      if(l.eq.0.or.q.eq.m-1.or.q.eq.m) Return

      if(q.lt.0.or.q.gt.m) Stop 'Itrans: wrong q'
      if(l.lt.0) Stop 'Itrans: wrong l'

      if(l.gt.6.and.q.gt.2) Stop 'Itrans: unconsidered case'
      if(l.eq.3.and.q.gt.4) Stop 'Itrans: unconsidered case'

      m = l*100 + q
      Select case(m)
       Case(102);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_p2,ic_p2)
       Case(103);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_p3,ic_p3)
       Case(104);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_p4,ic_p4)
       Case(202);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_d2,ic_d2)
       Case(203);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_d3,ic_d3)
       Case(204);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_d4,ic_d4)
       Case(205);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_d5,ic_d5)
       Case(206);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_d6,ic_d6)
       Case(207);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_d7,ic_d7)
       Case(208);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_d8,ic_d8)
       Case(302);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_f2,ic_f2)
       Case(303);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_f3,ic_f3)
       Case(304);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_f4,ic_f4)
       Case(402);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_g2,ic_g2)
       Case(502);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_h2,ic_h2)
       Case(602);  Itrans=Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn_i2,ic_i2)
      End Select

      End Function Itrans


!======================================================================
      Integer Function Jtrans(nj1,nj2,kt1,kt2,JT,kt,C,nn,ic)
!======================================================================

      Implicit none
      Integer, intent(in) :: nj1,nj2,kt1,kt2,JT,kt,nn,ic(8,nn)
      Integer :: i,k
      Real(8) :: C

      k=0; C=0.d0; Jtrans=0
      Do i=1,nn
       if(nj1.ne.ic(1,i)) Cycle
       if(nj2.ne.ic(2,i)) Cycle
       if(kt1.ne.ic(3,i)) Cycle
       if(kt2.ne.ic(4,i)) Cycle
       if(JT .ne.ic(5,i)) Cycle
       k = k + 1
       if(kt.eq.k) then
        Jtrans = ic(6,i)
        C = dfloat(ic(7,i))/dfloat(ic(8,i))
        C = dsqrt(abs(C))
        if(ic(7,i).lt.0) C = -C
        Return
      end if          
      End do
      Jtrans = k

      End Function Jtrans




